from __future__ import annotations

import logging
import uuid
from datetime import datetime
from typing import Any, Dict, Iterator, List, Optional

logger = logging.getLogger(__name__)


class WasteDatabase:
    """Complaint database for the waste-management application."""

    def __init__(
        self,
        store: Any,
        config: Optional[Dict[str, Any]] = None,
    ) -> None:
        self._store = store
        self._cfg   = config or {}
        self._waste_type = self._cfg.get("waste_type", None)
        logger.debug("WasteDatabase ready (store=%s)", type(store).__name__)

    def log_pickup_complaint(
        self, waste_type: Any, truck_id: Any, **extra: Any
    ) -> Dict[str, Any]:
        """Create and persist a new Complaint record."""
        record: Dict[str, Any] = {
            "id":         str(uuid.uuid4()),
            "waste_type":   waste_type,
            "truck_id":   truck_id,
            "status":     "active",
            "created_at": datetime.utcnow().isoformat(),
            **extra,
        }
        saved = self._store.put(record)
        logger.info("log_pickup_complaint: created %s", saved["id"])
        return saved

    def get_complaint(self, record_id: str) -> Optional[Dict[str, Any]]:
        """Retrieve a Complaint by its *record_id*."""
        record = self._store.get(record_id)
        if record is None:
            logger.debug("get_complaint: %s not found", record_id)
        return record

    def report_complaint(
        self, record_id: str, **changes: Any
    ) -> Dict[str, Any]:
        """Apply *changes* to an existing Complaint."""
        record = self._store.get(record_id)
        if record is None:
            raise KeyError(f"Complaint not found: {record_id}")
        record.update(changes)
        record["updated_at"] = datetime.utcnow().isoformat()
        return self._store.put(record)

    def reroute_complaint(self, record_id: str) -> bool:
        """Remove a Complaint record; returns True if deleted."""
        if self._store.get(record_id) is None:
            return False
        self._store.delete(record_id)
        logger.info("reroute_complaint: removed %s", record_id)
        return True

    def list_complaints(
        self,
        status: Optional[str] = None,
        limit:  int = 50,
        offset: int = 0,
    ) -> List[Dict[str, Any]]:
        """Return a filtered, paginated list of Complaint records."""
        query: Dict[str, Any] = {}
        if status:
            query["status"] = status
        results = self._store.find(query, limit=limit, offset=offset)
        logger.debug("list_complaints: %d results", len(results))
        return results

    def iter_complaints(
        self, batch_size: int = 100
    ) -> Iterator[Dict[str, Any]]:
        """Yield all Complaint records in batches of *batch_size*."""
        offset = 0
        while True:
            page = self.list_complaints(limit=batch_size, offset=offset)
            if not page:
                break
            yield from page
            if len(page) < batch_size:
                break
            offset += batch_size
